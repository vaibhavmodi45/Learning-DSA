#include<iostream>
using namespace std;

int main() {
    float r = 5.0; // radius of the circle
    float pi = 3.14; // value of pi
    float area; // variable to store the area of the circle

    area = pi * r * r; // calculate the area of the circle

    cout << "Area of the circle: " << area << endl; // output the area
    return 0; // return 0
}